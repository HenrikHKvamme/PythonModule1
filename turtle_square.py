import turtle

myTurtle = turtle.Turtle()

rounds = 1
while rounds > 0:
    myTurtle.forward(100)
    myTurtle.left(135)
    myTurtle.forward(141)
    myTurtle.left(135)
    myTurtle.forward(100)
    myTurtle.left(90)
    myTurtle.forward(100)
    myTurtle.left(90)
    myTurtle.forward(100)
    myTurtle.left(90)
    myTurtle.forward(100)
    myTurtle.left(135)
    myTurtle.forward(141)
    rounds = rounds - 1
turtle.done()
